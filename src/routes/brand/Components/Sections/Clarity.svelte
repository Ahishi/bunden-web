    <div class="my-16">
        <h4 class="text-2xl font-medium">Clear Space</h4>

        <h5 class="font-medium text-xl mt-8 mb-4">Logos</h5>
        <p>
            Logos should always have clear space around them for at least according to the margins.
            (picture above)
        </p>
        <p>These margins are:</p>
        <div>
            <li>Above and Below margins should be at least 2/13 (~15%) of the total height of the
                logo on each side.
            </li>
            <li>Side margins should always be at least 1/38 (~3%) of the total width of the logo on
                each side.
            </li>
        </div>
        <h5 class="font-medium text-xl mt-8 mb-4">Icons</h5>
        <p>
            As well as logos, icons should always have clear space around them for at least
            according to the margins. (picture above)
        </p>
        <p>
            Icons shall always occupy a space as a square with the width and the height being equal
            to the height of the visible icon.
        </p>
        <p>
            These margins are:
        </p>
        <div>
            <li>Above and Below margins should be at least 1/13 (~8%) of the total height of the
                icon on each side.
            </li>
            <li>Side margins should always be at least 1/13 (~8%) of the total square width of the
                logo on each side.
            </li>
        </div>
    </div>

    <h4 class="text-2xl font-medium mb-2">Sizing</h4>
    <p>
        The minimum size of the Bunden logo and icon is defined by the height of the symbol and
        should never be used below 12px/5mm. Although it is suggested that the logo is not regularly
        used at these sizes.
    </p>

